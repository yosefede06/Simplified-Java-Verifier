package oop.ex6.main.DataStructure;

/**
 * Class of Code
 */
public class Code {
    public String code;
    public String codeOutOfMethod;

    /**
     * Constructor of Code
     * @param code the code inside of methods
     * @param codeOutOfMethod the code outside of methods
     */
    public Code(String code, String codeOutOfMethod) {
        this.code = code;
        this.codeOutOfMethod = codeOutOfMethod;
    }
}
